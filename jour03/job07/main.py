def determiner_domaine(langage):
    if langage == "JavaScript":
        print("Tu es un développeur web.")
    elif langage == "Python":
        print("Tu es un développeur IA.")
    elif langage == "Java":
        print("Tu es un développeur logiciel.")
    elif langage == "ReactJS":
        print("Tu es un développeur mobile.")
    else:
        print("Un jour, je serai le meilleur développeur...")


determiner_domaine("JavaScript")
determiner_domaine("Python")
determiner_domaine("Java")
determiner_domaine("ReactJS")
determiner_domaine("C++") 
